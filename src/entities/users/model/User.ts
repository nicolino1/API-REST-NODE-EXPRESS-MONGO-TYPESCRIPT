import mongoose from "mongoose";

// creating user model as mongoose schema
const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        min: 6,
        max: 255
    },
    email: {
        type: String,
        required: true,
        min: 6,
        max: 255
    },
    password: {
        type: String,
        required: true,
        min: 6,
        max: 1024
    },
});

// export user model as User
export default mongoose.model("User", userSchema);
